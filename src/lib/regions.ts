export function findRegionByCode(code: string): RegionSeed | undefined {
  return REGIONS.find((r0) => r0.region_code === code);
}

export function nearestRegion(lat: number, lng: number): RegionSeed {
  let best = REGIONS[0];
  let bestDist = Infinity;
  for (const region of REGIONS) {
    const d = (region.lat - lat) ** 2 + (region.lng - lng) ** 2;
    if (d < bestDist) {
      bestDist = d;
      best = region;
    }
  }
  return best;
}

