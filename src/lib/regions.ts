import type { RegionSeed } from "@/types";

// 지역 시드 데이터.
// - region_code는 행정안전부 공식 법정동코드가 아닌, 이 앱 내부에서만 쓰는 단순 식별자다.
//   (설계서 10.2 regions.region_code는 실제로는 법정동/행정동코드를 쓰도록 되어 있으나,
//    현재는 DB/공식 코드표 연동 전이므로 "시도-시군구[-읍면동]" 슬러그로 대체한다.)
// - 좌표는 각 지자체 청사 기준 근사값이며, 5km 격자 기반 동네예보 조회 및 위치선택 UI 용도로는 충분하다.
// - 전국 250여개 시군구를 모두 담았으나, 세종시만 읍면동 단위까지 세분화했다(설계서 4.1 예시 지역).
//   다른 지역의 읍면동 세분화 및 공식 법정동코드 전체 매핑은 추후 Supabase 연동 시 보강 권장.

function r(sido: string, sigungu: string | undefined, lat: number, lng: number, eupmyeondong?: string): RegionSeed {
  const parts = [sido, sigungu, eupmyeondong].filter(Boolean);
  const code = parts
    .join("-")
    .replace(/\s+/g, "")
    .normalize("NFC");
  return {
    region_code: code,
    sido,
    sigungu,
    eupmyeondong,
    label: parts.join(" "),
    lat,
    lng,
  };
}

